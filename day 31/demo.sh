#!/usr/bin/env bash
# Демо для видео: ассистент разработчика (RAG по документации + git через MCP).
# Скриптованная сессия REPL: git-инструменты MCP напрямую, потом /help-вопросы —
# о структуре (RAG), о живом состоянии git (MCP), о схеме данных (docs)
# и вопрос-ловушка, на который в проекте ответа нет.
set -euo pipefail
cd "$(dirname "$0")"

printf '%s\n' \
  '/branch' \
  '/files day 31/docs' \
  '/diff' \
  '/help Как устроена структура репозитория? Что лежит в папках day N?' \
  '/help На какой я ветке и какие последние коммиты?' \
  '/help Какие MCP-инструменты есть у дня 31 и что они делают?' \
  '/help Какие поля у чанка в индексе документации дня 31?' \
  '/help Есть ли в проекте деплой в Kubernetes?' \
  '/quit' \
| ./run.sh
