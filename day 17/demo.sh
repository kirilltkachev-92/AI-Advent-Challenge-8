#!/usr/bin/env bash
#
# demo.sh — сценарий для записи видео-доказательства (День 17).
#
# Прогоняет приложение-агент по шагам, подавая команды в REPL с паузами,
# чтобы их было видно на экране. Над каждым шагом печатает баннер с тем,
# какой пункт задания он доказывает.
#
# Запуск (просто нажмите Record в ⌘⇧5 и выполните):
#   ./demo.sh
#
# Темп можно подстроить под свой темп речи переменными окружения:
#   DEMO_BOOT=8 DEMO_PAUSE=4 DEMO_AGENT=20 ./demo.sh
#
set -euo pipefail
cd "$(dirname "$0")"

# Паузы (сек): загрузка сервера, обычный шаг, ответ агента (ходит в LLM — дольше).
BOOT="${DEMO_BOOT:-7}"
PAUSE="${DEMO_PAUSE:-4}"
AGENT="${DEMO_AGENT:-18}"

# Баннер пишем напрямую в терминал (/dev/tty), а не в stdin приложения,
# чтобы он появился на экране, но не был воспринят как команда REPL.
banner() {
  { printf '\n\033[1;36m== %s ==\033[0m\n' "$*" > /dev/tty; } 2>/dev/null || true
}

# Поток команд для REPL: печатаем строку в stdin приложения, ждём паузу.
feed() {
  sleep "$BOOT"  # дать серверу подняться и агенту подключиться

  banner "Шаг 1/3 — регистрация инструментов + описание входных параметров"
  printf 'list\n';                                          sleep "$PAUSE"

  banner "Шаг 2/3 — агент САМ вызывает MCP-инструмент и использует результат"
  printf 'Какая погода в Париже и стоит ли брать зонт?\n';  sleep "$AGENT"

  banner "Шаг 3/3 — прямой вызов инструмента (возврат результата, без LLM)"
  printf 'tool get_weather {"city":"Токио"}\n';             sleep "$PAUSE"

  banner "Готово — выходим"
  printf 'quit\n';                                          sleep 1
}

feed | ./run.sh
