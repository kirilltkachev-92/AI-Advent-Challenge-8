/**
 * Контрольный вопрос из «усиления» задания: сам вопрос, ожидание (что должно быть
 * в ответе) и какие источники (разделы статьи) должны быть использованы.
 * Разделы сверяются с метаданным `section` извлечённых чанков.
 */
data class ControlQuestion(
    val question: String,
    val expectation: String,
    val expectedSections: List<String>,
)

/**
 * Мини-набор из 10 контрольных вопросов по базе (статья GPT-3, arXiv 2005.14165).
 * Смесь точечных фактов (цифры, которые модель без RAG обычно перевирает)
 * и «обзорных» вопросов по конкретным разделам.
 */
