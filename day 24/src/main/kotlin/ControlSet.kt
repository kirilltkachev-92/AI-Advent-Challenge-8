    val QUESTIONS = listOf(
        ControlQuestion(
            question = "How many parameters does the largest GPT-3 model have, and how many models in total were trained for the paper?",
            expectation = "175 billion parameters; 8 models were trained (from 125M to 175B).",
            expectedSections = listOf("1 Introduction", "2.1 Model and Architectures"),
        ),
        ControlQuestion(
            question = "Which datasets make up GPT-3's training mix and with what sampling weights?",
            expectation = "Common Crawl (filtered) 60%, WebText2 22%, Books1 8%, Books2 8%, Wikipedia 3%.",
            expectedSections = listOf("2.2 Training Dataset"),
        ),
        ControlQuestion(
            question = "What exactly do zero-shot, one-shot and few-shot mean in the paper, and how many examples K are typically used in few-shot?",
            expectation = "Settings without weight updates: 0 / 1 / K demonstrations in context; K is typically 10-100, bounded by the 2048-token context window.",
            expectedSections = listOf("2 Approach", "1 Introduction"),
        ),
        ControlQuestion(
            question = "What accuracy does GPT-3 achieve on LAMBADA in the few-shot setting and how does it compare to the previous state of the art?",
