#!/usr/bin/env bash
# Демо для видео: ассистент по файлам проекта. Скриптованная сессия REPL:
# сброс рабочей копии, поиск использований компонента (только чтение),
# обновление устаревшего README по коду (запись + diff), проверка правил
# из RULES.md с исправлением нарушений, итоговый /diff за сессию.
set -euo pipefail
cd "$(dirname "$0")"

printf '%s\n' \
  '/reset' \
  'Найди все места, где используется HttpFetcher, и составь список «файл:строка — что там происходит». Файлы не меняй.' \
  'Обнови docs/README.md, чтобы он точно соответствовал текущему коду в src и правилам из RULES.md.' \
  'Проверь файлы src на соответствие правилам проекта из RULES.md и исправь найденные нарушения.' \
  '/diff' \
  '/quit' \
| ./run.sh
