#!/usr/bin/env bash
#
# demo.sh — сценарий для записи видео-доказательства (День 20, оркестрация MCP).
#
# Показывает ДЛИННЫЙ КРОСС-СЕРВЕРНЫЙ флоу: на один запрос пользователя агент-оркестратор
# сам выбирает инструменты с ЧЕТЫРЁХ разных MCP-серверов (research / weather / report /
# storage), строит правильный порядок вызовов и передаёт данные между ними.
#
# Запуск (нажмите Record в ⌘⇧5 и выполните):
#   ./demo.sh
#
set -euo pipefail
cd "$(dirname "$0")"

PAUSE="${DEMO_PAUSE:-3}"
FLOW="${DEMO_FLOW:-55}"   # время на длинный флоу (6–8 кросс-серверных вызовов + ответ LLM)

banner() {
  { printf '\n\033[1;36m== %s ==\033[0m\n' "$*" > /dev/tty; } 2>/dev/null || true
}

feed() {
  sleep 8   # четыре сервера поднимаются, роутер подключается ко всем

  banner "Список инструментов по ЧЕТЫРЁМ MCP-серверам (research / weather / report / storage)"
  printf 'list\n'; sleep "$PAUSE"

  banner "Длинный флоу №1 — ДОСЬЕ: справка (research) + погода (weather) → отчёт (report) → файл (storage)"
  printf 'Собери досье по Санкт-Петербургу: справка из Википедии и текущая погода, собери отчёт и сохрани в spb.md, затем покажи список файлов\n'
  sleep "$FLOW"

  banner "Длинный флоу №2 — другой город, тот же оркестратор сам перестраивает порядок вызовов"
  printf 'Сделай такое же досье по Казани и сохрани в kazan.md\n'
  sleep "$FLOW"

  banner "Прочитать сохранённый отчёт обратно через storage-MCP (маршрутизация прямого вызова)"
  printf 'tool read_file {"filename":"spb.md"}\n'; sleep "$PAUSE"

  printf 'quit\n'; sleep 1
}

rm -rf output
feed | ./run.sh

# После выхода — показать созданные отчёты (оба файла собраны из данных РАЗНЫХ серверов).
banner "Доказательство: два досье, каждое собрано из данных нескольких MCP-серверов"
ls -la output 2>/dev/null || true
echo "----- output/spb.md (первые строки) -----"
head -12 output/spb.md 2>/dev/null || echo "(файл не найден)"
